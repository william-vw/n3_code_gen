package wvw.semweb.codegen.visit;

public class VisitModelException extends Exception {

	private static final long serialVersionUID = 1L;

	public VisitModelException() {
		super();
	}

	public VisitModelException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public VisitModelException(String message, Throwable cause) {
		super(message, cause);
	}

	public VisitModelException(String message) {
		super(message);
	}

	public VisitModelException(Throwable cause) {
		super(cause);
	}

}
